    public void LlenarLista(){
        
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        
        modelo.setRowCount(0);
        
        for (ClsAcceso MiAcceso : ClsDB.jsonAcceso) {
            Object[] row = new Object[]{MiAcceso.Activo,MiAcceso.Correo};
            modelo.addRow(row);
        }
        
    }

