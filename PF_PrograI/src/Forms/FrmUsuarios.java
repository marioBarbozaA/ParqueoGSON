    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        
        int columna = tblUsuarios.getColumnModel().getColumnIndexAtX(evt.getX());
        int fila = evt.getY()/tblUsuarios.getRowHeight();
        
        if (columna == 0) {
            
            if (fila > 0) {
                
                String Estado = "ACTIVAR";

                if (ClsDB.jsonAcceso.get(fila).Activo) {
                    Estado = "DESACTIVAR";
                }

                int resultado = JOptionPane.showConfirmDialog(null, "Esta seguro que desea " + Estado + " al usuario: " + ClsDB.jsonAcceso.get(fila).Correo, "Cambiar estado de usuario", JOptionPane.YES_NO_OPTION);

                if (resultado == JOptionPane.YES_OPTION) {

                    ClsDB.jsonAcceso.get(fila).Activo = !ClsDB.jsonAcceso.get(fila).Activo;
                    MisAccesos.GuardarDatosMemoria();

                }
                
            }else{
                JOptionPane.showMessageDialog(null, 
                        "El usuario administrador no puede ser eliminado", 
                         "Error de datos", 
                         JOptionPane.ERROR_MESSAGE);
            }
            
            LlenarLista();
        }
        
    }//GEN-LAST:event_tblUsuariosMouseClicked

