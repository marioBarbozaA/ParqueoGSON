    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        this.setAlwaysOnTop(false);

        if (ValidarDatos()) {

            if (IdEditar > -1) {

                ClsDB.jsonEnvios.get(IdEditar).Direccion = txtDireccion.getText();
                ClsDB.jsonEnvios.get(IdEditar).Monto = Integer.parseInt(txtCosto.getValue().toString());
                
                ClsDB.jsonEnvios.get(IdEditar).Recibido = chkRecibido.isSelected();
                ClsDB.jsonEnvios.get(IdEditar).Enviado = chkEnviado.isSelected();
                ClsDB.jsonEnvios.get(IdEditar).Entregado = chkEntregado.isSelected();
                        
                ClsDB.jsonEnvios.get(IdEditar).MiCliente.clear();
                ClsDB.jsonEnvios.get(IdEditar).MiConductor.clear();
                ClsDB.jsonEnvios.get(IdEditar).MiVehiculo.clear();
                
                ClsDB.jsonEnvios.get(IdEditar).MiCliente.add(ClsDB.jsonClientes.get(cmbCliente.getSelectedIndex()-1));
                ClsDB.jsonEnvios.get(IdEditar).MiConductor.add(ClsDB.jsonConductores.get(cmbConductor.getSelectedIndex()-1));
                ClsDB.jsonEnvios.get(IdEditar).MiVehiculo.add(ClsDB.jsonVehiculos.get(cmbVehiculo.getSelectedIndex() -1));
                
            }else{
                MisEnvios.IdEnvio = ClsDB.jsonEnvios.size() + 1;
                MisEnvios.Direccion = txtDireccion.getText();
                MisEnvios.Monto = Integer.parseInt(txtCosto.getValue().toString());
                
                MisEnvios.Recibido = chkRecibido.isSelected();
                MisEnvios.Enviado = chkEnviado.isSelected();
                MisEnvios.Entregado = chkEntregado.isSelected();
                
                MisEnvios.MiCliente.add(ClsDB.jsonClientes.get(cmbCliente.getSelectedIndex()-1));
                MisEnvios.MiConductor.add(ClsDB.jsonConductores.get(cmbConductor.getSelectedIndex()-1));
                MisEnvios.MiVehiculo.add(ClsDB.jsonVehiculos.get(cmbVehiculo.getSelectedIndex()-1));

                ClsDB.jsonEnvios.add(MisEnvios);
            }

            if (MisEnvios.GuardarDatosMemoria()) {

                JOptionPane.showMessageDialog(null,
                    "Los datos del cliente se han guardado con éxito",
                    "Proceso exitoso",
                    JOptionPane.INFORMATION_MESSAGE);

                this.dispose();
            }

        }else{
            JOptionPane.showMessageDialog(null,
                "Por favor ingrese toda la información solicitada",
                "Error de datos",
                JOptionPane.ERROR_MESSAGE);
            this.setAlwaysOnTop(true);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

