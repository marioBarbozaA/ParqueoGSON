    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        
        cmbCliente.removeAllItems();
        cmbCliente.addItem("Seleccione un cliente");
        for (ClsClientes MiCliente : ClsDB.jsonClientes) {
            cmbCliente.addItem(MiCliente.Nombre + " " + MiCliente.Apellidos);
        }
        
        if (!strCliente.equals("")) {
            cmbCliente.setSelectedItem(strCliente);
        }
        
        cmbConductor.removeAllItems();
        cmbConductor.addItem("Seleccione un conductor");
        for (ClsConductores MiConductor : ClsDB.jsonConductores) {
            cmbConductor.addItem(MiConductor.Nombre + " " + MiConductor.Apellidos);
        }
        
        if (!strConductor.equals("")) {
            cmbConductor.setSelectedItem(strConductor);
        }
        
        cmbVehiculo.removeAllItems();
        cmbVehiculo.addItem("Seleccione un vehículo");
        for (ClsVehiculos MiVehiculo : ClsDB.jsonVehiculos) {
            cmbVehiculo.addItem(MiVehiculo.Placa);
        }
        
        if (!strPlaca.equals("")) {
            cmbVehiculo.setSelectedItem(strPlaca);
        }
    }//GEN-LAST:event_formWindowActivated

