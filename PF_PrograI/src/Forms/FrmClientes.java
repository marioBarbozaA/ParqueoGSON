    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int fila = evt.getY()/tblClientes.getRowHeight();
        IdEditar =  Integer.parseInt(tblClientes.getValueAt(fila, 0).toString());
    }//GEN-LAST:event_tblClientesMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        if (IdEditar > 0 && IdEditar <= ClsDB.jsonClientes.size()) {
            
            if (!fAgregarCliente.isShowing()) {
                
                int Contador = 0;
                
                for (ClsClientes MiCliente : ClsDB.jsonClientes) {
                    if (MiCliente.IdCliente == IdEditar) {
                        IdEditar = Contador;
                        break;
                    }
                    Contador++;
                }
                
                fAgregarCliente.IdEditar = IdEditar;
                
                fAgregarCliente.txtCedula.setText(ClsDB.jsonClientes.get(IdEditar).Cedula);
                fAgregarCliente.txtNombre.setText(ClsDB.jsonClientes.get(IdEditar).Nombre);
                fAgregarCliente.txtApellidos.setText(ClsDB.jsonClientes.get(IdEditar).Apellidos);
                fAgregarCliente.txtCorreo.setText(ClsDB.jsonClientes.get(IdEditar).Correo);
                fAgregarCliente.txtTelefono.setText(ClsDB.jsonClientes.get(IdEditar).Telefono);
                fAgregarCliente.btnGuardar.setText("EDITAR CLIENTE");
                    
                fAgregarCliente.show();
                ClsGlobales.formPrincipal.enable(false);
            }
            
        }else{
            JOptionPane.showMessageDialog(null, 
                    "Por favor seleccione un cliente para editar", 
                    "Editar", 
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnEditarActionPerformed


