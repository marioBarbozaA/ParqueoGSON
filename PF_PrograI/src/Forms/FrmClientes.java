    public void LlenarLista(String buscar){
        
        DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
        
        modelo.setRowCount(0);
        
        for (ClsClientes MiCliente : ClsDB.jsonClientes) {
            Object[] row = new Object[]{MiCliente.IdCliente,
                                        MiCliente.Cedula,
                                        MiCliente.Nombre,
                                        MiCliente.Apellidos,
                                        MiCliente.Telefono,
                                        MiCliente.Correo};
            
            if (buscar.equals("")) {
                modelo.addRow(row);
            }else if (MiCliente.Cedula.toLowerCase().contains(buscar) ||
                      MiCliente.Nombre.toLowerCase().contains(buscar) ||
                      MiCliente.Apellidos.toLowerCase().contains(buscar) ||
                      MiCliente.Telefono.toLowerCase().contains(buscar) ||
                      MiCliente.Correo.toLowerCase().contains(buscar)) {
                      modelo.addRow(row);
            }
        }
    }

