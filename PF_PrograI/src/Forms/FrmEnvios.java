    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        if (!fAgregarEnvios.isShowing()) {

            IdEditar = -1;
            fAgregarEnvios.IdEditar = -1;
            fAgregarEnvios.txtDireccion.setText("");
            fAgregarEnvios.chkRecibido.setSelected(true);
            fAgregarEnvios.chkEnviado.setSelected(false);
            fAgregarEnvios.chkEntregado.setSelected(false);
            fAgregarEnvios.cmbCliente.setSelectedIndex(0);
            fAgregarEnvios.cmbConductor.setSelectedIndex(0);
            fAgregarEnvios.btnGuardar.setText("GUARDAR ENVIO");

            fAgregarEnvios.show();
            ClsGlobales.formPrincipal.enable(false);
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

