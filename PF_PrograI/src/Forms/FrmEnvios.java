    public void LlenarLista(String buscar){
        
        DefaultTableModel modelo = (DefaultTableModel) tblEnvios.getModel();
        
        modelo.setRowCount(0);
        
        for (ClsEnvios MiEnvio : ClsDB.jsonEnvios) {
            
            boolean Bandera = true;
            
            if (chkPendientes.isSelected() && 
                MiEnvio.Entregado) {
                Bandera = false;
            }
            
            if (Bandera) {
                Object[] row = new Object[]{MiEnvio.IdEnvio,
                                        MiEnvio.Direccion,
                                        MiEnvio.MiCliente.get(0).Nombre + " " + MiEnvio.MiCliente.get(0).Apellidos,
                                        MiEnvio.MiCliente.get(0).Telefono,
                                        MiEnvio.MiConductor.get(0).Nombre + " " + MiEnvio.MiConductor.get(0).Apellidos,
                                        MiEnvio.MiVehiculo.get(0).Placa,
                                        MiEnvio.MiConductor.get(0).Telefono,
                                        MiEnvio.Recibido,
                                        MiEnvio.Enviado,
                                        MiEnvio.Entregado};
            
                if (buscar.equals("")) {
                    modelo.addRow(row);
                }else if (MiEnvio.Direccion.toLowerCase().contains(buscar) ||
                          MiEnvio.MiCliente.get(0).Nombre.toLowerCase().contains(buscar) ||
                          MiEnvio.MiCliente.get(0).Telefono.toLowerCase().contains(buscar) ||
                          MiEnvio.MiConductor.get(0).Nombre.toLowerCase().contains(buscar) ||
                          MiEnvio.MiConductor.get(0).Telefono.toLowerCase().contains(buscar)) {
                          modelo.addRow(row);
                }
            }
        }
    }
