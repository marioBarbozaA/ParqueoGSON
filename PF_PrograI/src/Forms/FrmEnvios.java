    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (IdEditar > 0 && IdEditar <= ClsDB.jsonEnvios.size()) {

            if (!fAgregarEnvios.isShowing()) {

                int Contador = 0;

                for (ClsEnvios MiEnvio : ClsDB.jsonEnvios) {
                    if (MiEnvio.IdEnvio == IdEditar) {
                        IdEditar = Contador;
                        break;
                    }
                    Contador++;
                }

                fAgregarEnvios.IdEditar = IdEditar;

                fAgregarEnvios.txtDireccion.setText(ClsDB.jsonEnvios.get(IdEditar).Direccion);
                fAgregarEnvios.txtCosto.setValue(ClsDB.jsonEnvios.get(IdEditar).Monto);
                fAgregarEnvios.chkRecibido.setSelected(ClsDB.jsonEnvios.get(IdEditar).Recibido);
                fAgregarEnvios.chkEnviado.setSelected(ClsDB.jsonEnvios.get(IdEditar).Enviado);
                fAgregarEnvios.chkEntregado.setSelected(ClsDB.jsonEnvios.get(IdEditar).Entregado);
                
                fAgregarEnvios.btnGuardar.setText("EDITAR ENVIO");

                fAgregarEnvios.strCliente = ClsDB.jsonEnvios.get(IdEditar).MiCliente.get(0).Nombre + " " + ClsDB.jsonEnvios.get(IdEditar).MiCliente.get(0).Apellidos;
                fAgregarEnvios.strConductor = ClsDB.jsonEnvios.get(IdEditar).MiConductor.get(0).Nombre + " " + ClsDB.jsonEnvios.get(IdEditar).MiConductor.get(0).Apellidos;
                fAgregarEnvios.strPlaca = ClsDB.jsonEnvios.get(IdEditar).MiVehiculo.get(0).Placa;
                
                fAgregarEnvios.show();
                ClsGlobales.formPrincipal.enable(false);
            }

        }else{
            JOptionPane.showMessageDialog(null,
                "Por favor seleccione un cliente para editar",
                "Editar",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnEditarActionPerformed

